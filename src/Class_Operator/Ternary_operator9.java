package Class_Operator;

// Ternary (? :)
// Condition1 ? Value1 : Value 2

public class Ternary_operator9 {
    public static void main(String[] args) {
        int age = 30;
        String s = age < 15 ? "Do Medical Check-Up" : "Join Sports";
        // 30 < 15
        System.out.println(s);

        // Nested Ternary

        // condition1 ? (Con2 ? Value1 : Value 2) : (Con2 ? Value1 : Value 2);

    }
}
