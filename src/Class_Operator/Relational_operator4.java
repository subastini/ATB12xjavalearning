package Class_Operator;

/* Relational operator always gives results in Boolean
* bcz <    <=   >   >=   ==(Compare values)   !=(Not equal to)            */

public class Relational_operator4 {

    public static void main(String[] args) {

        int a = 10;
        int b = 60;

        // When relational operators are used the the data type should be boolean
        boolean c = a!=b; // Not like (int  c = a != b;)

        System.out.println(c);




    }
}
