package Ex_10_For_loop;

public class Break_Forloop {
    public static void main(String[] args) {

        for (int i = 0; i < 50; i++) {
           // System.out.println(i); //output --> 1,2,3,4,5
            if (i == 5){
                System.out.println(i); // output -> 5
                break;

            }
    }
    }
}

