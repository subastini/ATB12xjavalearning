package Ex_10_For_loop;

public class Decreament {
    public static void main(String[] args) {

        for (int i = -1; i <= -10 ; i++) {

            // i =-1 --> -1<=-10 --> F
            // -10 , -5, - 2, -1 , 0 , 1, 2, 3, 4, 5 ..... right numbers get bigger

            System.out.println(i);
        }
    }
}
